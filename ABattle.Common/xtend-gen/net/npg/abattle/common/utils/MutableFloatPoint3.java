package net.npg.abattle.common.utils;

@SuppressWarnings("all")
public class MutableFloatPoint3 {
  public float x;
  
  public float y;
  
  public float z;
  
  public MutableFloatPoint3(final float x, final float y, final float z) {
    this.x = x;
    this.y = y;
    this.z = z;
  }
}
